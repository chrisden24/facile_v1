<script>
    (function() {
        const sticky = document.getElementById("sticky-box");
        const stickyParent = document.getElementById("sticky-parent");
        const leftCol = document.getElementById("left-col");
        const topOffset = 100;

        function updateSticky() {
            const isMobile = window.innerWidth < 768; // disable on mobile
            if (isMobile) {
                sticky.style.position = "static";
                sticky.style.width = "100%";
                return;
            }

            const leftRect = leftCol.getBoundingClientRect();
            const parentRect = stickyParent.getBoundingClientRect();
            const stickyRect = sticky.getBoundingClientRect();
            const scrollTop = window.pageYOffset || document.documentElement.scrollTop;

            const leftTop = scrollTop + leftRect.top;
            const leftBottom = leftTop + leftRect.height;
            const stickyHeight = stickyRect.height;
            const parentWidth = parentRect.width;

            const fixedStart = leftTop;
            const fixedEnd = leftBottom - stickyHeight;

            if (scrollTop + topOffset > fixedStart && scrollTop < fixedEnd) {
                sticky.style.position = "fixed";
                sticky.style.top = topOffset + "px";
                sticky.style.width = parentWidth + "px";
            } else if (scrollTop >= fixedEnd) {
                sticky.style.position = "absolute";
                sticky.style.top = (leftCol.offsetHeight - stickyHeight) + "px";
                //sticky.style.width = "100%";
            } else {
                sticky.style.position = "static";
                sticky.style.width = "100%";
            }
        }

        window.addEventListener("scroll", updateSticky, {
            passive: true
        });
        window.addEventListener("resize", updateSticky);
        window.addEventListener("load", updateSticky);
    })();
</script>



